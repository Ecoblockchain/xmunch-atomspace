package com.xmunch.atomspace.model;

public class Atom {
	private String id;
	private String atomType;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAtomType() {
		return atomType;
	}
	public void setAtomType(String atomType) {
		this.atomType = atomType;
	}
}
